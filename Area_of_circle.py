#to find the area of circle of radius "r".
r=float(input("the radius of circle is : "))
area=float(3.14*r*r)
diameter=float(2.0*r)
circumfrence=float(2*3.14*r)
radius=r
print("the area is :",area)
print("the diameter is :",diameter)
print("the circumfrence is :",circumfrence)
print("the radius is: ",radius)
